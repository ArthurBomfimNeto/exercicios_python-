prinum = int(input('Digite o primeiro numero: '))
segnum = int(input('Digite o segundo numero: '))

if prinum > segnum :
    print('''O primeiro numero é maior!!
    ''')
elif segnum > prinum :
    print('''O segundo numero é maior!!
    ''')
elif segnum == prinum:
    print('''Os numeros são iguais!!
    ''')
else:
    print('Digito incorreto ')




